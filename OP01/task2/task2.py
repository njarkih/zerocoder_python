"""
Написать программу, которая будет запрашивать у пользователя его имя и возраст,
а затем выводить приветствие “Привет [имя]! Тебе [возраст]”.
Можно усовершенствовать программу, чтобы она посчитала также количество прожитых месяцев, дней, часов.
Просить помощи и разъяснений у ChatGPT, чтобы усовершенствовать программу — можно и нужно.

"""


def get_age_suffix(age):
    """Возвращает правильное склонение слова 'год' в зависимости от возраста."""
    if 11 <= (age % 100) <= 14:
        return "лет"
    elif age % 10 == 1:
        return "год"
    elif age % 10 in [2, 3, 4]:
        return "года"
    else:
        return "лет"


def main():
    name = input("Введите ваше имя: ")
    age = int(input("Введите ваш возраст: "))

    months = age * 12
    days = months * 30
    hours = days * 24

    age_suffix = get_age_suffix(age)

    print(f"Привет, {name}! Тебе {age} {age_suffix}, это {months} месяцев, {days} дней, {hours} часов.")


if __name__ == "__main__":
    main()
